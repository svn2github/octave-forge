%% -*- texinfo -*-
%% @deftypefn {Function File} {@var{tf} = } eq (@var{a}, @var{b})
%% Equal to operator for two Quaternions. Used by Octave for "a == b"
%%
%% @end deftypefn

function flg = eq (a, b)

  flg = a.q_wrap == b.q_wrap;

endfunction
